package agenda07exercicio2;

public class Agenda07Exercicio2 {

    public static void main(String[] args) {
        int tabuada;
      
      for(int i=6; i<=10; i++) //operadores de 6 até 10
      {
      for(int k = 1; k <=10; k++){  //operadores de 1 até 10
      tabuada = k*i;  //multiplicando os operadores do laço k com do laço i
      System.out.println(tabuada);
      } 
      }
    
    }
    
}
